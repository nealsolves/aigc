# ---------------------------------------------------------------------------
# Unified entry-point
# ---------------------------------------------------------------------------

def lint_target(path: str, *, kind: str = "auto") -> list[dict]:
    """
    Lint any supported target.

    Args:
        path: Path to the target (file or directory).
        kind: One of "auto", "policy", "starter_dir", "workflow_artifact",
              or "audit_artifact".
              When "auto", the kind is inferred from the path.

    Returns:
        List of finding dicts (empty = clean).
    """
    if kind == "auto":
        kind = detect_target_kind(path)

    if kind == "policy":
        return lint_policy(path)
    elif kind == "starter_dir":
        return lint_starter_dir(path)
    elif kind == "workflow_artifact":
        return lint_workflow_artifact(path)
    elif kind == "audit_artifact":
        return lint_audit_artifact(path)
    else:
        return [_finding(
            "POLICY_LOAD_ERROR",
            f"Cannot determine target kind for: {path}. "
            "Supported targets: .yaml/.yml policy files, starter directories "
            "(containing policy.yaml + workflow_example.py + README.md), "
            "workflow artifact .json files, and invocation audit artifact "
            ".json files.",
            "unknown",
            path,
        )]
